package task1

// Enum реализуется в Go следующим образом.

// Определяется кастомный тип на основе базового типа

type Sex uint8

// Определяются константы – значения enum'а
// iota используется для удобства
// Можно было бы просто проставить вручную значения (от 0 до 1 в данном случае)

const (
	Male Sex = iota
	Female
)

// Определяется функция String, которая возвращает строковое значение для каждой константы

func (s Sex) String() string {
	return [...]string{"Male", "Female"}[s]
}
